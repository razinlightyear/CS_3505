#include <QtWidgets>
#ifndef QT_NO_PRINTER
#include <QPrinter>
#include <QPrintDialog>
#include <QDebug>
#endif

#include "scribblearea.h"

ScribbleArea::ScribbleArea(QWidget *parent)
    : QWidget(parent)
{
    setAttribute(Qt::WA_StaticContents);
    modified = false;
    scribbling = false;
    myPenWidth = 1;
    myPenColor = Qt::blue;
    currFrameIndex = 0;
    frames.append(image);
    qDebug() << Q_FUNC_INFO << "currFrameIndex" << currFrameIndex << "frames.length()"<< frames.size();;
}

bool ScribbleArea::openImage(const QString &fileName)
{
    QImage loadedImage;
    if (!loadedImage.load(fileName))
        return false;

    QSize newSize = loadedImage.size().expandedTo(size());
    resizeImage(&loadedImage, newSize);
    image = loadedImage;
    modified = false;
    update();
    return true;
}

bool ScribbleArea::saveImage(const QString &fileName, const char *fileFormat)
{
    QImage visibleImage = image;
    resizeImage(&visibleImage, size());

    if (visibleImage.save(fileName, fileFormat)) {
        modified = false;
        return true;
    } else {
        return false;
    }
}

void ScribbleArea::setPenColor(const QColor &newColor)
{
    myPenColor = newColor;
}

void ScribbleArea::setPenWidth(int newWidth)
{
    myPenWidth = newWidth;
}

void ScribbleArea::clearImage()
{
    image.fill(qRgb(255, 255, 255));
    modified = true;
    update();
}

void ScribbleArea::mousePressEvent(QMouseEvent *event)
{
    if (event->button() == Qt::LeftButton) {
        lastPoint = event->pos();
        scribbling = true;
    }
}

void ScribbleArea::mouseMoveEvent(QMouseEvent *event)
{
    if ((event->buttons() & Qt::LeftButton) && scribbling)
    {
        drawLineTo(event->pos());
        qDebug() << Q_FUNC_INFO << event->pos();
    }
}

void ScribbleArea::mouseReleaseEvent(QMouseEvent *event)
{
    if (event->button() == Qt::LeftButton && scribbling) {
        drawLineTo(event->pos());
        scribbling = false;
        frames[currFrameIndex] = image;
    }
}

void ScribbleArea::paintEvent(QPaintEvent *event)
{
    QPainter painter(this);
    QRect dirtyRect = event->rect();
    painter.drawImage(dirtyRect, image, dirtyRect);
    qDebug() << Q_FUNC_INFO << dirtyRect.size();
}

void ScribbleArea::resizeEvent(QResizeEvent *event)
{
    if (width() > image.width() || height() > image.height()) {
        int newWidth = qMax(width() + 128, image.width());
        int newHeight = qMax(height() + 128, image.height());
        resizeImage(&image, QSize(newWidth, newHeight));
        update();
    }
    QWidget::resizeEvent(event);
}

void ScribbleArea::drawLineTo(const QPoint &endPoint)
{
    QPainter painter(&image);
    painter.setPen(QPen(myPenColor, myPenWidth, Qt::SolidLine, Qt::RoundCap,
                        Qt::RoundJoin));
    painter.drawLine(lastPoint, endPoint);
    modified = true;

    int rad = (myPenWidth / 2) + 2;
    update(QRect(lastPoint, endPoint).normalized()
                                     .adjusted(-rad, -rad, +rad, +rad));
    lastPoint = endPoint;
    QRgb rgb = image.pixel(endPoint);
    qDebug() << Q_FUNC_INFO << "Alpha" << qAlpha(rgb) << "Red" << qRed(rgb) << "Green" << qGreen(rgb) << "Blue" << qBlue(rgb); //pixel(lastPoint,endPoint);
}

void ScribbleArea::resizeImage(QImage *image, const QSize &newSize)
{
    if (image->size() == newSize)
        return;

    QImage newImage(newSize, QImage::Format_RGB32);
    newImage.fill(qRgb(255, 255, 255));
    QPainter painter(&newImage);
    painter.drawImage(QPoint(0, 0), *image);
    *image = newImage;
}

void ScribbleArea::print()
{
#if !defined(QT_NO_PRINTER) && !defined(QT_NO_PRINTDIALOG)
    QPrinter printer(QPrinter::HighResolution);

    QPrintDialog printDialog(&printer, this);
    if (printDialog.exec() == QDialog::Accepted) {
        QPainter painter(&printer);
        QRect rect = painter.viewport();
        QSize size = image.size();
        size.scale(rect.size(), Qt::KeepAspectRatio);
        painter.setViewport(rect.x(), rect.y(), size.width(), size.height());
        painter.setWindow(image.rect());
        painter.drawImage(0, 0, image);
    }
#endif // QT_NO_PRINTER
}

void ScribbleArea::next()
{
    qDebug() << Q_FUNC_INFO << "currFrameIndex" << currFrameIndex << "frames.length()"<< frames.size();
    if(currFrameIndex < frames.size()-1)
    {
        image = frames[++currFrameIndex];
    }
    update();
}

void ScribbleArea::prev()
{
    qDebug() << Q_FUNC_INFO << "currFrameIndex" << currFrameIndex << "frames.length()"<< frames.size();
    if(currFrameIndex > 0)
    {
        image = frames[--currFrameIndex];
    }
    update();
}

void ScribbleArea::newFrame()
{
    qDebug() << Q_FUNC_INFO << "currFrameIndex" << currFrameIndex << "frames.length()"<< frames.size();
    QImage nextImage;
    QSize newSize = nextImage.size().expandedTo(size());
    resizeImage(&nextImage, newSize);
    nextImage.fill(qRgb(255, 255, 255));
    frames.insert(++currFrameIndex,nextImage);
    image = nextImage;
    update();
}

void ScribbleArea::addReverse()
{
    qDebug() << Q_FUNC_INFO;
    for(int i=frames.size()-1; i>=0; i--)
    {
        frames.append(frames[i]);
    }
    update();
}
